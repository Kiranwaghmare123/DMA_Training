public class Array1 {
    public static void main(String args[]) {
        //Declaration of array
        int arr[] = new int[5];
        //Initialization of array
        arr[0] = 10;
        arr[1] = 20;
        arr[2] = 30;
        arr[3] = 40;
        arr[4] = 50;
        //Accessing an element of an array
        System.out.println("Element="+arr[2]);
    }
}
